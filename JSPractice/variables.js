/**
 * how to declare a variable:
 * ES4 - var
 * ES6 - let, const, var
 */

let s = 6
console.log(s)
console.log(typeof(s))

/**
 * data types in javascript
 * string, number, boolean, null and undefined
 */

let str = "Learn JS"
console.log(typeof(str))

let n = undefined
console.log(typeof(n))

let nu = null
console.log(typeof(nu))

str = 'adasdkja'
console.log(str)

/**
 * using let we cannot redeclare variable but can reassign already declared variable
 * using var we can reassign as well as redeclare variable
 * but using const we cannot reassign and redeclare variable
 */


